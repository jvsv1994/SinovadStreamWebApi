using SinovadDemo.Application.DTO;
using SinovadDemo.Application.Interface.Persistence;
using SinovadDemo.Application.Interface.UseCases;
using SinovadDemo.Application.Shared;
using SinovadDemo.Domain.Entities;
using SinovadDemo.Transversal.Common;
using SinovadDemo.Transversal.Mapping;

namespace SinovadDemo.Application.UseCases.Profiles
{
    public class ProfileService : IProfileService
    {
        private IUnitOfWork _unitOfWork;

        private readonly SharedService _sharedService;

        public ProfileService(IUnitOfWork unitOfWork, SharedService sharedService)
        {
            _unitOfWork = unitOfWork;
            _sharedService = sharedService;
        }

        public async Task<Response<ProfileDto>> GetAsync(int id)
        {
            var response = new Response<ProfileDto>();
            try
            {
                var result = await _unitOfWork.Profiles.GetAsync(id);
                response.Data = result.MapTo<ProfileDto>();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public async Task<ResponsePagination<List<ProfileDto>>> GetAllWithPaginationByAccountAsync(string accountId, int page, int take)
        {
            var response = new ResponsePagination<List<ProfileDto>>();
            try
            {
                var result = await _unitOfWork.Profiles.GetAllWithPaginationByExpressionAsync(page, take, "Id", true, x => x.AccountId == accountId);
                response.Data = result.Items.MapTo<List<ProfileDto>>();
                response.PageNumber = page;
                response.TotalPages = result.Pages;
                response.TotalCount = result.Total;
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public Response<object> Create(ProfileDto profileDto)
        {
            var response = new Response<object>();
            try
            {
                var profile = profileDto.MapTo<Profile>();
                _unitOfWork.Profiles.Add(profile);
                _unitOfWork.Save();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public Response<object> CreateList(List<ProfileDto> listProfileDto)
        {
            var response = new Response<object>();
            try
            {
                var profiles = listProfileDto.MapTo<List<Profile>>();
                _unitOfWork.Profiles.AddList(profiles);
                _unitOfWork.Save();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public Response<object> Update(ProfileDto profileDto)
        {
            var response = new Response<object>();
            try
            {
                var profile = profileDto.MapTo<Profile>();
                _unitOfWork.Profiles.Update(profile);
                _unitOfWork.Save();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public Response<object> Delete(int id)
        {
            var response = new Response<object>();
            try
            {
                _unitOfWork.Profiles.Delete(id);
                _unitOfWork.Save();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

        public Response<object> DeleteList(string ids)
        {
            var response = new Response<object>();
            try
            {
                List<int> listIds = new List<int>();
                if (!string.IsNullOrEmpty(ids))
                {
                    listIds = ids.Split(",").Select(x => Convert.ToInt32(x)).ToList();
                }
                _unitOfWork.Profiles.DeleteByExpression(x => listIds.Contains(x.Id));
                _unitOfWork.Save();
                response.IsSuccess = true;
                response.Message = "Successful";
            }
            catch (Exception ex)
            {
                response.Message = ex.Message;
                _sharedService._tracer.LogError(ex.StackTrace);
            }
            return response;
        }

    }
}
